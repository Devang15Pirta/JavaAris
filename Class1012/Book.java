package Class1012;

public class Book {
	String name;
	String id;
	String price;
	public Book(String name, String id, String price) {
		super();
		this.name = name;
		this.id = id;
		this.price = price;
	}
	public String getName() {
		return name;
	}
	public String getId() {
		return id;
	}
	public String getPrice() {
		return price;
	}

}
