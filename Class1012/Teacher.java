package Class1012;

public class Teacher {
	String name;
	String id;
	int salary;
	public Teacher(String name, String id, int salary) {
		super();
		this.name = name;
		this.id = id;
		this.salary = salary;
	}
	public String getName() {
		return name;
	}
	public String getId() {
		return id;
	}
	public int getSalary() {
		return salary;
	}

}
