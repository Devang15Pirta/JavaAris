package Class0812;

public class Book {
String name;
String id;
String price;
String author;
public Book(String name, String id, String price, String author) {
	super();
	this.name = name;
	this.id = id;
	this.price = price;
	this.author = author;
}
public String getName() {
	return name;
}
public String getId() {
	return id;
}
public String getPrice() {
	return price;
}
public String getAuthor() {
	return author;
}


}
