package Class0812;

public class Friend {
	String name;
	String fname;
	String pet;
	String contact;
	public Friend(String name, String fname,String pet, String contact) {
		super();
		this.pet=pet;
		this.name = name;
		this.fname = fname;
		this.contact = contact;
	}
	public String getName() {
		return name;
	}
	public String getFname() {
		return fname;
	}
	public String getContact() {
		return contact;
	}
	public String getPet() {
		return pet;
	}
	

}
